public class MemoriaRam {
    private int capacidadeGB;

    //Construtor
    public MemoriaRam(int capacidadeGB) {
        this.capacidadeGB = capacidadeGB;
    }

    //get e sett
    public int getCapacidadeGB() {
        return capacidadeGB;
    }

    public void setCapacidadeGB(int capacidadeGB) {
        this.capacidadeGB = capacidadeGB;
    }
}
